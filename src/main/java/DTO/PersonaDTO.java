package DTO;

/**
 *
 * @author crism
 */
public class PersonaDTO {
    Double id;
    String nombre;
    String apellido;
    String email;
    
    public PersonaDTO(Double id, String nombre, String apellido, String email) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
    }

    public Double getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public String getEmail() {
        return email;
    }
     
     
}
